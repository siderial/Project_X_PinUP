package com.example.undine.project_ooad;


public class Post {
    int id;
    String url;
    String title;
    String date;
    String author;
    String thumbnail;

// Getter and Setter

    public String getAuthor() {
        return author;
    }

    public int getId() {
        return id;
    }

    public String getDate() {
        return date;
    }

    public String getThumbnail() {
        return thumbnail;
    }

    public String getTitle() {
        return title;
    }

    public String getUrl() {
        return url;
    }
}